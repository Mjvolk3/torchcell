DOI = "10.1007/BF03195688"

# Born-digital source: the mirrored publisher PDF (library mirror). The sensitive-gene
# lists (Tables 1-6) are extracted from its text layer via ``pdftotext -layout``.
_LIBRARY_CITATION_KEY = "auesukareeGenomewideIdentificationGenes2009"
_PDF_FILENAME = "paper.pdf"
_PDF_SHA256 = "01b945443c0ce41642c76fd737e12b4c31cacb5f384049a5c0a7e4bf9e1eb5a1"

# Sensitivity is scored relative to the parental BY4742 strain (Methods).
_SENSITIVE_CATEGORY = "sensitive"
_REFERENCE_CATEGORY = "tolerant"

MEASUREMENT_UNITS = (
    "serial-dilution spot-assay growth call vs parental BY4742 after 3 days: "
    "'sensitive' = significantly reduced growth under stress vs non-stress conditions"
)

# n_samples: "All experiments for the identification of stress-sensitive mutants were
# performed in triplicate." (Methods). Triplicate independent screening experiments.
_N_SAMPLES = 3

# Per-table caption stress token (as it appears in the PDF captions) -> expected number of
# LISTED sensitive genes (the parenthetical class counts sum; a build-time self-checksum).
# Ethanol/1-propanol/heat/NaCl/H2O2 match the abstract headline; methanol Table 2 lists 55
# (abstract headline says 54 -- documented source discrepancy, Table is authoritative).
_EXPECTED_LISTED: dict[str, int] = {
    "ethanol": 95,
    "methanol": 55,
    "1-propanol": 125,
    "heat": 178,
    "NaCl": 42,
    "H2O2": 30,
}

# Per-stress environment spec. ``kind`` selects how the edit is modeled:
#   - "small_molecule": an added chemical SPECIES (the 3 alcohols + NaCl + H2O2 are all
#     small molecules -- NaCl/H2O2 are named compounds, NOT abstract "physical stresses").
#   - "temperature": NO perturbation object; the edit is the raised
#     ``Environment.temperature`` (37 C) alone -- temperature has ONE canonical home (M2).
# The physiological framing (osmotic / oxidative / alcohol) is a CONSEQUENCE, not an
# edit, so it is deliberately absent from the perturbation (M1).
_STRESS_SPECS: list[dict[str, Any]] = [
    {
        "stress": "ethanol",
        "kind": "small_molecule",
        "compound_name": "ethanol",
        "concentration": (10.0, "percent_v/v"),
        "temperature": 30.0,
    },
    {
        "stress": "methanol",
        "kind": "small_molecule",
        "compound_name": "methanol",
        "concentration": (16.0, "percent_v/v"),
        "temperature": 30.0,
    },
    {
        "stress": "1-propanol",
        "kind": "small_molecule",
        "compound_name": "1-propanol",
        "concentration": (7.0, "percent_v/v"),
        "temperature": 30.0,
    },
    {"stress": "heat", "kind": "temperature", "temperature": 37.0},
    {
        "stress": "NaCl",
        "kind": "small_molecule",
        "compound_name": "sodium chloride",
        "concentration": (1.0, "M"),
        "temperature": 30.0,
    },
    {
        "stress": "H2O2",
        "kind": "small_molecule",
        "compound_name": "hydrogen peroxide",
        "concentration": (5.0, "mM"),
        "temperature": 30.0,
    },
]

_CAPTION_RE = re.compile(
    r"^\s*Table [1-6]\. Classification of genes whose deletions result in "
    r"(.+?) sensitivity\s*$"
)
# A functional-class row: "<Class words> (<count>)   <gene, gene, ...>".
_CLASS_RE = re.compile(r"^\s*[A-Za-z].*?\((\d+)\)\s{2,}(\S.*)$")
_CONTINUATION_RE = re.compile(r"^\s{2,}\S")
_SYSTEMATIC_RE = re.compile(r"^Y[A-P][LR]\d{3}[WC](-[A-Z])?$")


def _tokenize(cell: str) -> list[str]:
    """Split a table gene cell into gene tokens (comma/whitespace separated)."""
    return [t for t in re.split(r"[,\s]+", cell.strip()) if t]


@register_dataset
class EnvChemgenAuesukaree2009Dataset(ExperimentDataset):
    """Auesukaree 2009 six-stress env x geno -> categorical sensitivity dataset."""

    def __init__(
        self,
        root: str = "data/torchcell/env_chemgen_auesukaree2009",
        io_workers: int = 0,
        genome: SCerevisiaeGenome | None = None,
        transform: Callable[..., Any] | None = None,
        pre_transform: Callable[..., Any] | None = None,
        **kwargs: Any,
    ) -> None:
        """Initialize the dataset; a genome is REQUIRED for common-name -> ORF mapping."""
        self.genome = genome
        super().__init__(root, io_workers, transform, pre_transform, **kwargs)

    @property
    def experiment_class(self) -> type[Experiment]:
        """Experiment schema class produced by this dataset."""
        return EnvironmentResponseExperiment

    @property
    def reference_class(self) -> type[ExperimentReference]:
        """Experiment-reference schema class produced by this dataset."""
        return EnvironmentResponseExperimentReference

    @property
    def raw_file_names(self) -> list[str]:
        """The mirrored publisher PDF (born-digital source of the sensitive-gene tables)."""
        return [_PDF_FILENAME]

    def download(self) -> None:
        """Copy the pinned library PDF into raw_dir and verify its sha256.

        The article tables ARE the data (no external data deposit); the canonical source is
        the sha256-pinned PDF in the local library mirror. PMC file downloads use a JS
        proof-of-work and are not scriptable, so the mirror is the reproducible source.
        """
        os.makedirs(self.raw_dir, exist_ok=True)
        dest = osp.join(self.raw_dir, _PDF_FILENAME)
        if not osp.exists(dest):
            data_root = os.environ["DATA_ROOT"]
            src = osp.join(
                data_root, "torchcell-library", _LIBRARY_CITATION_KEY, _PDF_FILENAME
            )
            if not osp.exists(src):
                raise RuntimeError(
                    f"library mirror PDF not found: {src}. This dataset's source is the "
                    f"sha256-pinned PDF in the torchcell-library mirror."
                )
            shutil.copyfile(src, dest)
        digest = hashlib.sha256(open(dest, "rb").read()).hexdigest()
        if digest != _PDF_SHA256:
            raise RuntimeError(
                f"{_PDF_FILENAME} sha256 mismatch: got {digest}, expected {_PDF_SHA256}"
            )
        log.info("Verified %s (sha256 %s)", dest, _PDF_SHA256)

    def _parse_tables(self) -> dict[str, list[str]]:
        """Extract {stress: [gene tokens]} from the born-digital PDF text layer.

        Uses ``pdftotext -layout`` (poppler); each functional-class row declares its member
        count in parentheses, used as a per-class self-checksum against extraction drift.
        """
        pdf_path = osp.join(self.raw_dir, _PDF_FILENAME)
        text = subprocess.run(
            ["pdftotext", "-layout", pdf_path, "-"],
            capture_output=True,
            text=True,
            check=True,
        ).stdout
        lines = text.split("\n")
        tables: dict[str, list[str]] = {}
        i = 0
        while i < len(lines):
            caption = _CAPTION_RE.match(lines[i])
            if not caption:
                i += 1
                continue
            stress = caption.group(1)
            i += 1
            genes: list[str] = []
            cur_declared = 0
            cur_count = 0
            while i < len(lines):
                line = lines[i]
                if line.strip() == "" or _CAPTION_RE.match(line):
                    break
                class_row = _CLASS_RE.match(line)
                if class_row:
                    cur_declared = int(class_row.group(1))
                    toks = _tokenize(class_row.group(2))
                    cur_count = len(toks)
                    genes.extend(toks)
                elif _CONTINUATION_RE.match(line) and cur_count < cur_declared:
                    toks = _tokenize(line)
                    cur_count += len(toks)
                    genes.extend(toks)
                else:
                    # not a table row (footer / prose) -> table body ended
                    break
                i += 1
            tables[stress] = genes
        return tables

    def _resolver(self) -> Callable[[str], str | None]:
        """Build a gene-name -> current-R64-systematic-ORF resolver from the genome."""
        if self.genome is None:
            raise RuntimeError(
                "EnvChemgenAuesukaree2009Dataset requires a genome for gene-name "
                "resolution; inject SCerevisiaeGenome(...)"
            )
        genome = self.genome
        df = genome.gene_attribute_table
        ids = set(df["ID"])
        alias_map = genome.alias_to_systematic

        def resolve(token: str) -> str | None:
            gene = token.upper()
            if gene in ids:
                return gene
            # An old systematic name (matches the pattern but is not a current R64 ID):
            # follow the genome alias table to the current systematic name.
            if _SYSTEMATIC_RE.match(gene):
                candidates = alias_map.get(gene, [])
                if candidates and candidates[0] in ids:
                    return candidates[0]
            hit = df[df["gene"] == gene]
            if not hit.empty:
                return str(hit.iloc[0]["ID"])
            hit = df[df["Alias"] == gene]
            if not hit.empty:
                return str(hit.iloc[0]["ID"])
            candidates = alias_map.get(gene, [])
            if candidates and candidates[0] in ids:
                return candidates[0]
            return None

        return resolve

    def _resolve_stress(
        self, stress: str, tokens: list[str], resolve: Callable[[str], str | None]
    ) -> dict[str, str]:
        """Resolve one stress's gene tokens to {ORF: source_token}; checksum + log drops."""
        expected = _EXPECTED_LISTED[stress]
        if len(tokens) != expected:
            raise RuntimeError(
                f"{stress}: parsed {len(tokens)} listed genes, expected {expected} "
                f"(table extraction self-checksum failed)"
            )
        resolved: dict[str, str] = {}
        dropped: list[str] = []
        for token in tokens:
            orf = resolve(token)
            if orf is None:
                dropped.append(token)
                continue
            resolved.setdefault(orf, token)
        log.info(
            "Auesukaree2009 %s: %d listed -> %d unique-ORF records (dropped %d: %s)",
            stress,
            len(tokens),
            len(resolved),
            len(set(dropped)),
            sorted(set(dropped)),
        )
        return resolved

    def _environment(self, spec: dict[str, Any]) -> Environment:
        """Aerobic YPD plate carrying the edit (an added small molecule, or raised heat).

        The 5 chemical stresses are ``SmallMoleculePerturbation``s; heat carries NO
        perturbation -- its edit is the raised ``Environment.temperature`` alone (M2).
        """
        perturbations: list[
            SmallMoleculePerturbation
            | EnvironmentPhysicalPerturbation
            | BiologicPerturbation
        ] = []
        if spec["kind"] == "small_molecule":
            value, unit = spec["concentration"]
            perturbations.append(
                SmallMoleculePerturbation(
                    compound=resolved_compound(spec["compound_name"]),
                    concentration=Concentration(
                        value=value, unit=ConcentrationUnit(unit)
                    ),
                )
            )
        return Environment(
            media=Media(name="YPD", state="solid", is_synthetic=False),
            temperature=Temperature(value=spec["temperature"]),
            perturbations=perturbations,
            aerobicity="aerobic",
            duration_hours=72.0,
        )

    def _reference(
        self, environment: Environment
    ) -> EnvironmentResponseExperimentReference:
        """Parental BY4742 baseline: tolerant (no significant growth reduction)."""
        phenotype_reference = EnvironmentResponsePhenotype(
            measurement_type=MeasurementType.categorical,
            category=_REFERENCE_CATEGORY,
            n_samples=_N_SAMPLES,
            sample_unit=SampleUnit.biological_replicate,
            units=MEASUREMENT_UNITS,
        )
        return EnvironmentResponseExperimentReference(
            dataset_name=self.name,
            genome_reference=ReferenceGenome(
                species="Saccharomyces cerevisiae", strain="BY4742"
            ),
            environment_reference=environment.model_copy(),
            phenotype_reference=phenotype_reference,
        )

    def _experiment(
        self, *, orf: str, token: str, environment: Environment
    ) -> EnvironmentResponseExperiment:
        """Build one env x geno -> categorical-sensitivity experiment for (gene, stress)."""
        genotype = Genotype(
            perturbations=[
                KanMxDeletionPerturbation(
                    systematic_gene_name=orf, perturbed_gene_name=token
                )
            ]
        )
        phenotype = EnvironmentResponsePhenotype(
            measurement_type=MeasurementType.categorical,
            category=_SENSITIVE_CATEGORY,
            n_samples=_N_SAMPLES,
            sample_unit=SampleUnit.biological_replicate,
            units=MEASUREMENT_UNITS,
        )
        return EnvironmentResponseExperiment(
            dataset_name=self.name,
            genotype=genotype,
            environment=environment,
            phenotype=phenotype,
        )

    @post_process
    def process(self) -> None:
        """Parse the six stress tables into categorical records; write LMDB."""
        resolve = self._resolver()
        tables = self._parse_tables()
        publication = Publication(doi=DOI, doi_url=f"https://doi.org/{DOI}")
        pub_dump = publication.model_dump()

        os.makedirs(self.preprocess_dir, exist_ok=True)
        os.makedirs(self.processed_dir, exist_ok=True)
        env = lmdb.open(osp.join(self.processed_dir, "lmdb"), map_size=int(1e11))
        idx = 0
        with env.begin(write=True) as txn:
            for spec in _STRESS_SPECS:
                stress = spec["stress"]
                if stress not in tables:
                    raise RuntimeError(
                        f"stress table not found in PDF: {stress!r} "
                        f"(parsed: {sorted(tables)})"
                    )
                environment = self._environment(spec)
                reference = self._reference(environment)
                ref_dump = reference.model_dump()
                orf_tokens = self._resolve_stress(stress, tables[stress], resolve)
                for orf, token in tqdm(sorted(orf_tokens.items()), desc=f"{stress}"):
                    experiment = self._experiment(
                        orf=orf, token=token, environment=environment
                    )
                    txn.put(
                        f"{idx}".encode(),
                        pickle.dumps(
                            {
                                "experiment": experiment.model_dump(),
                                "reference": ref_dump,
                                "publication": pub_dump,
                            }
                        ),
                    )
                    idx += 1
        env.close()
        log.info(
            "Wrote %d Auesukaree2009 environment-response experiments to LMDB", idx
        )

    def preprocess_raw(self, df: Any, preprocess: dict[str, Any] | None = None) -> Any:
        """Preprocessing is handled inside process() for this dataset."""
        return df

    def create_experiment(self) -> None:
        """Experiment construction is handled inline in process() for this dataset."""
        raise NotImplementedError


def main() -> None:
    """Build/load the dataset for interactive debugging."""
    from dotenv import load_dotenv

    load_dotenv()
    data_root = os.environ["DATA_ROOT"]
    genome = SCerevisiaeGenome(
        genome_root=osp.join(data_root, "data/sgd/genome"),
        go_root=osp.join(data_root, "data/go"),
        overwrite=False,
    )
    root = osp.join(data_root, "data/torchcell/env_chemgen_auesukaree2009")
    dataset = EnvChemgenAuesukaree2009Dataset(root=root, genome=genome)
    print(f"len = {len(dataset)}")
    print(dataset[0])


if __name__ == "__main__":
    main()
