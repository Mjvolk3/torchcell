"""Auesukaree 2009 multi-stress tolerance screen (env x geno -> categorical response).

Auesukaree et al. 2009 (J Appl Genet 50(3):301-310, doi:10.1007/BF03195688; PMC2747848)
screened the ~4828-strain S. cerevisiae nonessential HAPLOID single-deletion collection
(Saccharomyces Genome Deletion Project / Research Genetics-Invitrogen, BY4742 MATalpha
background) for growth SENSITIVITY to six environmental stresses on YPD plates: three
alcohols (10% ethanol, 16% methanol, 7% 1-propanol; all v/v), heat (37 C), high osmolarity
(1 M NaCl), and oxidative stress (5 mM H2O2). This is a plain haploid deletion collection
(NOT a HIP/HOP heterozygous screen), so every screened strain is a single-gene KanMX
deletion in BY4742 and there is no engineered-heterozygous / dosage complication.

The readout is a CATEGORICAL sensitivity call by serial-dilution spot assay: "Deletion
mutants showing significantly reduced growth on plates under stress conditions, as compared
to those under non-stress conditions, were defined as sensitive mutants" (Methods). The
paper reports ONLY the SENSITIVE hits (enumerated per stress in Tables 1-6, grouped by
functional class); the non-sensitive majority (score = tolerant) is NOT listed. So this
dataset carries one categorical ``sensitive`` record per (sensitive deletion x stress) pair;
the parental BY4742 strain (tolerant) is the reference. No mutant conferred INCREASED
tolerance in this study (only sensitivity was scored), so there is no "resistant" category.

This maps onto the WS15 environment-perturbation ontology. ``EnvironmentResponseExperiment``
= single-deletion ``Genotype`` (a ``KanMxDeletionPerturbation`` in the BY4742 reference
background) x aerobic ``Environment`` carrying the EDIT (never its consequence, M1):
- the three alcohols AND NaCl AND H2O2 are all ``SmallMoleculePerturbation``s -- an added
  chemical species dosed at a ``Concentration`` (ethanol 10% v/v, methanol 16% v/v,
  1-propanol 7% v/v, sodium chloride 1 M, hydrogen peroxide 5 mM). NaCl / H2O2 are named
  compounds, not abstract "physical stresses"; their osmotic / oxidative framing is a
  CONSEQUENCE and lives on the phenotype / the compound's ChEBI role, not the edit.
- heat carries NO perturbation object: its edit is the raised ``Environment.temperature``
  (37 C vs the 30 C base), the single canonical home for temperature (M2).
The readout is ``EnvironmentResponsePhenotype(measurement_type=categorical,
category="sensitive")``; the reference phenotype is ``category="tolerant"``.

PROVENANCE / SOURCING (born-digital, sha256-pinned; see class constants + provenance record):
- Background (confirmed haploid, NOT HIP/HOP): "The 4828 nonessential haploid S. cerevisiae
  deletion strains generated by the Saccharomyces Genome Deletion Project (Winzeler et al.
  1999) were obtained from Research Genetics/Invitrogen. These strains are on a BY4742
  (MATalpha his3-1 leu2-0 lys2-0 ura3-0) background (Brachmann et al. 1998)." (Methods,
  "Strains and media"). The BY4742 auxotrophies are the standard collection background ->
  captured by ``ReferenceGenome(strain="BY4742")`` (default ploidy haploid), NOT modeled as
  perturbations (Costanzo/Kuzmin/Mota convention); genotype = one KanMxDeletionPerturbation.
  No constant-background screened-gene deletions.
- Conditions / concentrations: "strains were stamped onto YPD plates and YPD plates
  containing 10% ethanol, 16% methanol, 7% propanol, 1 M NaCl, or 5 mM H2O2, and grown at
  30 C for a further 3 days. Screening for heat-sensitive deletion mutants was performed at
  37 C." (Methods, "Screening for stress-sensitive deletion mutants"). These are the maximum
  non-inhibitory concentrations/temperature determined by the wild-type spot test (Results,
  Figure 1). YPD solid plates, 30 C (37 C for heat), 3 days = 72 h, aerobic.
- Categorical scoring: "Deletion mutants showing significantly reduced growth on plates
  under stress conditions, as compared to those under non-stress conditions, were defined as
  sensitive mutants." (Methods). category = "sensitive"; reference (parental) = "tolerant".
- n_samples: "All experiments for the identification of stress-sensitive mutants were
  performed in triplicate." (Methods) -> n_samples = 3. The paper does not specify whether
  the triplicate is biological or technical; each replicate is an independent screening
  experiment of the mutant collection, so ``sample_unit=biological_replicate`` is used (the
  independent-experiment reading). Categorical readout carries no SE, so no uncertainty.

DATA SOURCE (the sensitive-gene lists themselves):
- The per-stress sensitive-gene lists are the article Tables 1-6, extracted deterministically
  from the BORN-DIGITAL text layer of the mirrored publisher PDF
  ($DATA_ROOT/torchcell-library/auesukareeGenomewideIdentificationGenes2009/paper.pdf,
  sha256 pinned below) via ``pdftotext -layout`` (poppler). There is no separate data deposit
  or supplementary spreadsheet -- the data IS the article tables. Each table row declares its
  functional-class member count in parentheses (e.g. "Vacuolar function (16)"); the parser
  uses these declared counts as per-class self-checksums, so any extraction drift is caught
  at build time. (The functional classification is NOT stored -- only the gene x stress
  sensitivity call is needed.) The MinerU OCR ``paper.md`` in the same mirror is a lossy
  fallback: its heat Table 4 "Unknown function" cell is truncated by two ORFs
  (YOR364w, YPL144w); the born-digital PDF text layer is complete, so it is the source of
  record here (matching the project born-digital-extraction strategy).

SOURCE COUNT NOTES (documented, not guessed):
- Per-stress listed sensitive genes match the paper's abstract/Figure headline counts EXACTLY
  for five stresses: ethanol 95, 1-propanol 125, heat 178, NaCl 42, H2O2 30.
- METHANOL is the one internal source discrepancy: the abstract/Figure 2 headline says 54
  methanol-sensitive mutants, but Table 2 lists 55 distinct genes and its functional-class
  parenthetical counts also sum to 55 (11+7+4+5+2+5+2+2+4+5+1+7). The Table (the actual
  per-gene data) is authoritative over the summary headline, so 55 methanol records are
  stored; the 1-gene headline-vs-table discrepancy is flagged (a paper-internal count error).
- Every one of the 525 listed gene tokens resolves to a unique current SGD R64 systematic
  ORF via the genome alias table (zero drops). One paper-era systematic name, YGR272c (heat),
  is an old identifier the genome alias table maps to current R64 YGR271C-A; it is resolved
  (not dropped). Final: ethanol 95, methanol 55, 1-propanol 125, heat 178, NaCl 42, H2O2 30
  = 525 categorical records.
"""

