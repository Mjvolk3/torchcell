"""
Create LMDB dataset for inference_dataset_3 from parquet triples.

This script loads triple combinations from the parquet file generated by
generate_triple_combinations_inference_3.py and creates an LMDB dataset
suitable for model inference.

Key differences from inference_dataset_2:
- Uses triples from 010-kuzmin-tmi/inference_3/raw/
- Filtered with relaxed thresholds: max(SMF) > 1.04, max(DMF) > 1.08
- Baselines: all(SMF) > 0.80, all(DMF) > 0.80
- Output to 010-kuzmin-tmi/inference_3/processed/lmdb/
"""

import os
import os.path as osp
import sys

import pandas as pd
from dotenv import load_dotenv

from torchcell.sequence.genome.scerevisiae.s288c import SCerevisiaeGenome
from torchcell.timestamp import timestamp

# Add scripts directory to path for import
sys.path.insert(0, osp.dirname(osp.abspath(__file__)))
from inference_dataset_2 import InferenceDataset


def main():
    """Main function to create inference dataset from parquet triples."""
    load_dotenv()
    DATA_ROOT = os.getenv("DATA_ROOT")

    ts = timestamp()
    print(f"Starting inference_dataset_3 creation at {ts}")
    print("=" * 80)
    print("Thresholds: max(SMF) > 1.04, max(DMF) > 1.08")
    print("Baselines: all(SMF) > 0.80, all(DMF) > 0.80")
    print("=" * 80)

    # Paths - pointing to inference_3 directory
    inference_dir = osp.join(
        DATA_ROOT, "data/torchcell/experiments/010-kuzmin-tmi/inference_3"
    )
    raw_dir = osp.join(inference_dir, "raw")

    parquet_path = osp.join(raw_dir, "triple_combinations_list.parquet")
    if not osp.exists(parquet_path):
        raise FileNotFoundError(
            f"Parquet file not found: {parquet_path}\n"
            f"Run generate_triple_combinations_inference_3.py first."
        )
    print(f"Using parquet file: {parquet_path}")

    df = pd.read_parquet(parquet_path)
    print(f"Total triples in parquet: {len(df):,}")

    print("\nInitializing genome...")
    genome_root = osp.join(DATA_ROOT, "data/sgd/genome")
    go_root = osp.join(DATA_ROOT, "data/go")
    genome = SCerevisiaeGenome(
        genome_root=genome_root,
        go_root=go_root,
        overwrite=False
    )
    genome.drop_empty_go()
    print(f"Genome gene set size: {len(genome.gene_set)}")

    print("\nCreating InferenceDataset...")
    dataset = InferenceDataset(
        root=inference_dir,
        gene_set=genome.gene_set,
        graphs=None,
        node_embeddings=None,
        graph_processor=None,
    )

    num_workers = min(int(os.environ.get("NUM_WORKERS", 16)), 64)
    print(f"\nLoading triples to LMDB (parallel with {num_workers} workers)...")
    dataset.load_from_parquet_parallel(
        parquet_path,
        num_workers=num_workers,
        chunk_size=5000,
        batch_commit_size=5_000_000
    )

    print("\n" + "=" * 80)
    print("INFERENCE DATASET 3 CREATION COMPLETE")
    print("=" * 80)
    print(f"Dataset root: {inference_dir}")
    print(f"LMDB location: {osp.join(inference_dir, 'processed', 'lmdb')}")
    print(f"Total experiments: {len(dataset):,}")
    print(f"Timestamp: {ts}")


if __name__ == "__main__":
    main()
