"""Generate the Fig-3 "one embedding, many phenotypes" build queries.

Emits two ``UNION ALL`` Cypher files into ``experiments/019-simb-multimodal/queries``:

* ``fig3_build.cql`` -- the FULL union of every requested dataset (expression +
  morphology + all single/double-mutant fitness incl. the large Dmf Kuzmin sets).
  This is the canonical, spec-faithful query artifact.
* ``fig3_core.cql`` -- the tractable multimodal substrate actually built by
  ``query_fig3.py``: it drops ``DmfKuzmin2018Dataset`` / ``DmfKuzmin2020Dataset``
  because, per the overlap census in ``query_fig3.py``, together they contribute
  ~1.04M double-mutant fitness records but ZERO additional genotypes that also
  carry expression (only 8 pairs overlap, all already covered elsewhere). The
  core build captures the entire expression<->fitness / expression<->morphology
  overlap at ~29k records instead of ~1.07M.

Design notes carried in every block:
* phenotype ``graph_level`` filter is per modality: expression = 'node',
  morphology = 'global', fitness = 'global'.
* fitness blocks additionally require ALL perturbations to be deletions
  (``perturbation_type ENDS WITH 'deletion'``) so a fitness record only merges
  onto an expression/morphology genotype when it is the SAME deletion strain --
  never a ts-allele / DAmP / allele of the same gene (those are distinct strains
  that the mean-deduplicator would otherwise force to ``mean_deletion``).
* media / temperature are MATCHed but NOT filtered -- Ohya is YPD/25C, the others
  SC or SGA-selection/30C or 26C; downstream WS6 conditions on the carried env, so
  no record is dropped on environment.
"""

from __future__ import annotations

import os.path as osp

# (dataset_id, graph_level, is_fitness)
EXPRESSION = [
    ("MicroarrayKemmeren2014Dataset", "node", False),
    ("SmMicroarraySameith2015Dataset", "node", False),
    ("DmMicroarraySameith2015Dataset", "node", False),
]
MORPHOLOGY = [
    ("ScmdOhya2005Dataset", "global", False),
]
FITNESS_SINGLE = [
    ("SmfCostanzo2016Dataset", "global", True),
    ("SmfKuzmin2018Dataset", "global", True),
    ("SmfKuzmin2020Dataset", "global", True),
]
FITNESS_DOUBLE_CORE = [
    ("DmfCostanzo2016Dataset", "global", True),
]
FITNESS_DOUBLE_LARGE = [
    ("DmfKuzmin2018Dataset", "global", True),
    ("DmfKuzmin2020Dataset", "global", True),
]

CORE_BLOCKS = EXPRESSION + MORPHOLOGY + FITNESS_SINGLE + FITNESS_DOUBLE_CORE
FULL_BLOCKS = CORE_BLOCKS + FITNESS_DOUBLE_LARGE


def _block(dataset_id: str, graph_level: str, is_fitness: bool) -> str:
    """Return one dataset ``MATCH ... RETURN e, ref`` block."""
    deletion_clause = ""
    if is_fitness:
        deletion_clause = (
            "\n AND ALL(dp IN [(g)<-[:PerturbationMemberOf]-(pert) | pert]\n"
            " WHERE dp.perturbation_type ENDS WITH 'deletion')"
        )
    return f"""// {dataset_id} (graph_level '{graph_level}')
MATCH (dataset:Dataset)<-[:ExperimentMemberOf]-(e:Experiment)
WHERE dataset.id = '{dataset_id}'
MATCH (e)<-[:GenotypeMemberOf]-(g:Genotype)
MATCH (g)<-[:PerturbationMemberOf]-(p:Perturbation)
MATCH (e)<-[:ExperimentReferenceOf]-(ref:ExperimentReference)
MATCH (e)<-[:PhenotypeMemberOf]-(phen:PhenotypicFeature)
MATCH (e)<-[:EnvironmentMemberOf]-(env:Environment)
MATCH (env)<-[:MediaMemberOf]-(m:Media)
MATCH (env)<-[:TemperatureMemberOf]-(t:Temperature)
WHERE phen.graph_level = '{graph_level}'
 AND ALL(gp IN [(g)<-[:PerturbationMemberOf]-(pert) | pert]
 WHERE gp.systematic_gene_name IN $gene_set)
 AND SIZE([(g)<-[:PerturbationMemberOf]-(pert) | pert]) > 0{deletion_clause}
WITH DISTINCT e, ref
 ORDER BY e.id
RETURN e, ref"""


def _render(blocks: list[tuple[str, str, bool]], header: str) -> str:
    body = "\n\nUNION ALL\n\n".join(_block(*b) for b in blocks)
    return f"{header}\n\n{body}\n"


def main() -> None:
    here = osp.dirname(osp.abspath(__file__))
    qdir = osp.abspath(osp.join(here, "..", "queries"))

    full_header = (
        "// Fig-3 FULL union: expression + morphology + all single/double fitness.\n"
        "// Generated by experiments/019-simb-multimodal/scripts/generate_fig3_cql.py\n"
        "// DmfKuzmin2018/2020 add ~1.04M records but 0 genotypes that also carry\n"
        "// expression -- see fig3_core.cql (the default build) and query_fig3.py."
    )
    core_header = (
        "// Fig-3 CORE union (default build): expression + morphology + single-mutant\n"
        "// fitness + DmfCostanzo double-mutant fitness. Drops DmfKuzmin2018/2020\n"
        "// (~1.04M records, 0 extra expression-overlapping genotypes).\n"
        "// Generated by experiments/019-simb-multimodal/scripts/generate_fig3_cql.py"
    )

    with open(osp.join(qdir, "fig3_build.cql"), "w") as f:
        f.write(_render(FULL_BLOCKS, full_header))
    with open(osp.join(qdir, "fig3_core.cql"), "w") as f:
        f.write(_render(CORE_BLOCKS, core_header))
    print(f"wrote {osp.join(qdir, 'fig3_build.cql')}")
    print(f"wrote {osp.join(qdir, 'fig3_core.cql')}")


if __name__ == "__main__":
    main()
