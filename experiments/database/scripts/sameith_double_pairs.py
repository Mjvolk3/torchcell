"""Emit the Sameith 2015 double-knockout gene pairs for targeted KG subsetting.

Sameith 2015 measures expression for double-knockout strains but reports no fitness.
The double-mutant fitness for those same pairs lives in the Costanzo 2016 screen --
but that dataset is 20.7M records, and the KG build caps it with a random sample, so a
0.48% cap hits essentially none of these specific pairs. This script writes the pair
list that `subset.include` feeds to `GeneSetSelector`, so the build keeps exactly the
Costanzo records that join to Sameith's expression profiles.

Output: torchcell/knowledge_graphs/conf/gene_sets/sameith_double_pairs.txt
  one pair per line, whitespace-separated systematic gene names, `#` comments allowed.

It is written INSIDE the package (not to experiments/results/) because the KG build runs
in a container that pip-installs torchcell from git -- `experiments/` is not present
there, so a build input has to ship with the package alongside the hydra configs that
reference it. This script remains the generator of record.
"""

import os
import os.path as osp

from dotenv import load_dotenv

import torchcell
from torchcell.datasets.scerevisiae.sameith2015 import DmMicroarraySameith2015Dataset

load_dotenv()
DATA_ROOT = os.environ["DATA_ROOT"]

OUT_PATH = osp.join(
    osp.dirname(torchcell.__file__),
    "knowledge_graphs/conf/gene_sets/sameith_double_pairs.txt",
)


def main() -> None:
    """Write the sorted, de-duplicated Sameith double-knockout gene pairs."""
    dataset = DmMicroarraySameith2015Dataset(
        root=osp.join(DATA_ROOT, "data/torchcell/dm_microarray_sameith2015")
    )
    pairs: set[tuple[str, ...]] = set()
    for record in dataset:
        perturbations = record["experiment"]["genotype"]["perturbations"]
        genes = tuple(sorted(p["systematic_gene_name"] for p in perturbations))
        pairs.add(genes)

    os.makedirs(osp.dirname(OUT_PATH), exist_ok=True)
    with open(OUT_PATH, "w") as fh:
        fh.write("# Sameith 2015 double-knockout gene pairs (systematic names).\n")
        fh.write(
            f"# Generated by {osp.basename(__file__)} from {len(dataset)} records.\n"
        )
        fh.write("# Consumed by subset.include -> GeneSetSelector (match: exact).\n")
        for genes in sorted(pairs):
            fh.write(" ".join(genes) + "\n")

    sizes = sorted({len(g) for g in pairs})
    print(f"records: {len(dataset)}  distinct gene sets: {len(pairs)}  sizes: {sizes}")
    print(f"wrote {OUT_PATH}")


if __name__ == "__main__":
    main()
