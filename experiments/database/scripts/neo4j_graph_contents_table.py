r"""Report what is REPRESENTED IN THE NEO4J GRAPH (not the source datasets).

The companion `build_supported_datasets_table.py` describes the source LMDBs going
IN; this script describes the graph that came OUT -- node labels + relationship
types + their counts, read live from the Neo4j `torchcell` database. Re-run it after
any build to regenerate the report against the current graph.

Run from the repo root (Neo4j must be up, e.g. the tc-neo4j-readonly container):
  python experiments/database/scripts/neo4j_graph_contents_table.py
  python experiments/database/scripts/neo4j_graph_contents_table.py --uri bolt://localhost:7687

Writes:
  experiments/database/results/graph-contents/<date>/graph_contents.json  (raw)
  notes/paper.neo4j-graph-contents.md                                     (readable)
and prints the markdown table.
"""

from __future__ import annotations

import argparse
import json
import os
from datetime import date
from pathlib import Path
from typing import Any

from dotenv import load_dotenv
from neo4j import GraphDatabase

load_dotenv()

SCRIPT = Path(__file__).resolve()
REPO = SCRIPT.parents[3]
RESULTS = SCRIPT.parent.parent / "results" / "graph-contents"
MD_OUT = REPO / "notes" / "paper.neo4j-graph-contents.md"

# BioLink roots every node carries; reported separately so the leaf-type table is
# the meaningful one (a node is counted under EACH of its labels).
BIOLINK_ROOTS = {"Entity", "NamedThing"}


def query_graph(uri: str, user: str, password: str, database: str) -> dict[str, Any]:
    """Read node-label, relationship-type, and total counts from the graph."""
    driver = GraphDatabase.driver(uri, auth=(user, password))
    try:
        with driver.session(database=database) as session:
            labels = session.run(
                "MATCH (n) UNWIND labels(n) AS l "
                "RETURN l AS label, count(*) AS n ORDER BY n DESC"
            ).data()
            rels = session.run(
                "MATCH ()-[r]->() "
                "RETURN type(r) AS rel, count(*) AS n ORDER BY n DESC"
            ).data()
            total_nodes = session.run("MATCH (n) RETURN count(n) AS n").single()["n"]
            total_edges = session.run(
                "MATCH ()-[r]->() RETURN count(r) AS n"
            ).single()["n"]
    finally:
        driver.close()
    return {
        "labels": labels,
        "relationships": rels,
        "total_nodes": total_nodes,
        "total_edges": total_edges,
    }


def render_markdown(data: dict[str, Any], built: str) -> str:
    """Render the graph-contents report as a Dendron-frontmattered markdown note."""
    lines = [
        "---",
        "id: neo4j-graph-contents",
        "title: Neo4j Graph Contents",
        "desc: What is represented in the built Neo4j torchcell graph (node/edge types)",
        "---",
        "",
        "# Neo4j Graph Contents",
        "",
        f"Live counts from the Neo4j `torchcell` database ({built}). "
        f"**{data['total_nodes']:,} nodes**, **{data['total_edges']:,} edges**. "
        "Generated by `experiments/database/scripts/neo4j_graph_contents_table.py`; "
        "re-run after any build.",
        "",
        "## Node types",
        "",
        "Each node is counted under every label it carries; `Entity` / `NamedThing` "
        "are the BioLink roots (= all nodes).",
        "",
        "| Node label | Count |",
        "|---|--:|",
    ]
    for row in data["labels"]:
        mark = " (BioLink root)" if row["label"] in BIOLINK_ROOTS else ""
        lines.append(f"| {row['label']}{mark} | {row['n']:,} |")
    lines += ["", "## Relationship types", "", "| Relationship | Count |", "|---|--:|"]
    for row in data["relationships"]:
        lines.append(f"| {row['rel']} | {row['n']:,} |")
    lines.append("")
    return "\n".join(lines)


def main() -> None:
    """Query the graph, write the JSON + markdown report, and print the table."""
    parser = argparse.ArgumentParser()
    parser.add_argument("--uri", default="bolt://localhost:7687")
    parser.add_argument("--user", default=os.getenv("NEO4J_USER", "neo4j"))
    parser.add_argument("--password", default=os.getenv("NEO4J_PASSWORD", "torchcell"))
    parser.add_argument("--database", default="torchcell")
    args = parser.parse_args()

    data = query_graph(args.uri, args.user, args.password, args.database)
    built = date.today().isoformat()

    out_dir = RESULTS / built
    out_dir.mkdir(parents=True, exist_ok=True)
    (out_dir / "graph_contents.json").write_text(json.dumps(data, indent=2) + "\n")

    md = render_markdown(data, built)
    MD_OUT.write_text(md)
    print(md)
    print(f"\nWrote {out_dir / 'graph_contents.json'} and {MD_OUT}")


if __name__ == "__main__":
    main()
